#!/usr/bin/env node
/**
 * Generates MANIFEST.md files for the feature-highlight captures.
 *
 * Walks apps/cassini/docs/feature-audit/<group>/<feature>/<NN>-<state>.png
 * and emits:
 *   - apps/cassini/docs/feature-audit/MANIFEST.md (top-level table)
 *   - apps/cassini/docs/feature-audit/<group>/MANIFEST.md (per-group index)
 *
 * Each entry includes:
 *   - feature ID
 *   - state name
 *   - relative file path (from feature-audit dir)
 *   - alt-text suggestion
 *   - marketing slot mapping (manual heuristic — see SLOT_MAP)
 *
 * Run after the Playwright project completes:
 *   pnpm feature-tour:manifest
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const FEATURE_AUDIT_DIR = path.resolve(__dirname, '../../../docs/feature-audit')

// Heuristic mapping of feature IDs to marketing slots. Hand-curated for
// the obvious matches; consumers (website, README) reference these.
const SLOT_MAP = {
  'A1-login-page': ['marketing/login-shot'],
  'B2-dashboard-single-char-variable': ['website/hero', 'README/dashboard'],
  'B3-attribute-charts': ['website/spc-attribute'],
  'B6-bottom-drawer': ['website/capability-card'],
  'B8-violations-view': ['website/violations'],
  'C1-show-your-work': ['website/show-your-work', 'README/syw'],
  'C2-audit-log': ['website/compliance/audit-log'],
  'D1-manual-entry': ['website/data-entry'],
  'D2-collection-plans': ['website/collection-plans'],
  'D4-import-wizard': ['website/csv-import'],
  'E1-monitor-tab': ['website/connectivity-monitor'],
  'E2-servers-tab': ['website/connectivity-servers'],
  'E4-mapping-tab': ['website/connectivity-mapping'],
  'F1-hierarchy-editor': ['website/configuration', 'README/hierarchy'],
  'G1-msa-list': ['website/msa-overview'],
  'G2-msa-crossed-anova': ['website/msa', 'README/msa'],
  'G5-doe-list': ['website/doe-overview'],
  'G6-doe-editor': ['website/doe', 'README/doe'],
  'G7-fai-list': ['website/fai'],
  'G8-fai-editor': ['website/fai-editor'],
  'I1-correlation': ['website/analytics-correlation'],
  'I2-multivariate': ['website/analytics-multivariate'],
  'I3-predictions': ['website/analytics-predictions'],
  'I4-ai-insights': ['website/ai-insights'],
  'J1-reports-view': ['website/reports', 'README/reports'],
  'K1-kiosk-view': ['website/kiosk'],
  'K2-wall-dashboard': ['website/wall-dashboard'],
  'K3-galaxy-view': ['website/galaxy'],
  'L5-license-settings': ['website/licensing'],
  'M1-cep-rules': ['website/cep-rules', 'README/cep'],
  'M2-sop-rag': ['website/sop-rag', 'README/rag'],
  'M3-lakehouse': ['website/lakehouse', 'README/lakehouse'],
  'N3-user-management': ['website/users'],
}

function altText(feature, state) {
  const featureLabel = feature
    .replace(/^[A-Z]\d+-/, '')
    .replace(/-/g, ' ')
    .replace(/\b\w/g, (c) => c.toUpperCase())
  const stateLabel = state.replace(/^\d+-/, '').replace(/-/g, ' ')
  return `Cassini ${featureLabel} — ${stateLabel}`
}

function listGroups() {
  if (!fs.existsSync(FEATURE_AUDIT_DIR)) return []
  return fs
    .readdirSync(FEATURE_AUDIT_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory() && /^[A-Z]$/.test(d.name))
    .map((d) => d.name)
    .sort()
}

function listFeatures(group) {
  const groupDir = path.join(FEATURE_AUDIT_DIR, group)
  if (!fs.existsSync(groupDir)) return []
  return fs
    .readdirSync(groupDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort()
}

function listStates(group, feature) {
  const featureDir = path.join(FEATURE_AUDIT_DIR, group, feature)
  if (!fs.existsSync(featureDir)) return []
  return fs
    .readdirSync(featureDir)
    .filter((f) => f.endsWith('.png'))
    .sort()
}

function buildGroupManifest(group) {
  const features = listFeatures(group)
  const lines = []
  lines.push(`# Group ${group} — Feature-Highlight Manifest`)
  lines.push('')
  lines.push('Auto-generated by `pnpm feature-tour:manifest`. Do not edit by hand.')
  lines.push('')
  lines.push('| Feature | State | File | Alt text | Marketing slot |')
  lines.push('|--------|-------|------|----------|----------------|')

  for (const feature of features) {
    const states = listStates(group, feature)
    for (const stateFile of states) {
      const stateName = stateFile.replace(/\.png$/, '')
      const relPath = path.posix.join(feature, stateFile)
      const slots = (SLOT_MAP[feature] || []).join(', ') || '—'
      lines.push(
        `| ${feature} | ${stateName} | \`${relPath}\` | ${altText(feature, stateName)} | ${slots} |`,
      )
    }
  }
  lines.push('')
  return lines.join('\n')
}

function buildTopLevelManifest() {
  const groups = listGroups()
  const lines = []
  lines.push('# Cassini Feature-Highlight — Master Manifest')
  lines.push('')
  lines.push('Auto-generated by `pnpm feature-tour:manifest`. Do not edit by hand.')
  lines.push('')
  lines.push('Walk every captured P0 / P1 state from `CATALOG.md`.')
  lines.push('')
  let total = 0
  lines.push('| Group | Features | States | Per-group manifest |')
  lines.push('|------:|--------:|------:|--------------------|')
  for (const group of groups) {
    const features = listFeatures(group)
    const stateCount = features.reduce((acc, f) => acc + listStates(group, f).length, 0)
    total += stateCount
    lines.push(
      `| ${group} | ${features.length} | ${stateCount} | [\`${group}/MANIFEST.md\`](${group}/MANIFEST.md) |`,
    )
  }
  lines.push('')
  lines.push(`Total captured states: **${total}**.`)
  lines.push('')
  lines.push('## Marketing slot index')
  lines.push('')
  lines.push('| Slot | Capture |')
  lines.push('|------|---------|')
  // Inverted index: slot → feature/state
  const allCaptures = []
  for (const group of groups) {
    for (const feature of listFeatures(group)) {
      for (const stateFile of listStates(group, feature)) {
        allCaptures.push({ group, feature, state: stateFile.replace(/\.png$/, '') })
      }
    }
  }
  for (const [feature, slots] of Object.entries(SLOT_MAP)) {
    const cap = allCaptures.find((c) => c.feature === feature)
    if (!cap) continue
    for (const slot of slots) {
      const path = `${cap.group}/${cap.feature}/${cap.state}.png`
      lines.push(`| ${slot} | \`${path}\` |`)
    }
  }
  lines.push('')
  return lines.join('\n')
}

function main() {
  if (!fs.existsSync(FEATURE_AUDIT_DIR)) {
    console.error(`Feature-audit directory not found: ${FEATURE_AUDIT_DIR}`)
    process.exit(1)
  }
  const groups = listGroups()
  if (groups.length === 0) {
    console.warn('No group directories found — nothing to manifest yet.')
    process.exit(0)
  }
  for (const group of groups) {
    const groupPath = path.join(FEATURE_AUDIT_DIR, group, 'MANIFEST.md')
    fs.writeFileSync(groupPath, buildGroupManifest(group))
    console.log(`Wrote ${groupPath}`)
  }
  const topPath = path.join(FEATURE_AUDIT_DIR, 'MANIFEST.md')
  fs.writeFileSync(topPath, buildTopLevelManifest())
  console.log(`Wrote ${topPath}`)
}

main()
