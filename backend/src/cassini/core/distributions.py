"""Non-normal distribution fitting and capability analysis.

PURPOSE:
    Provides three methods for computing process capability indices when the
    data does not follow a normal distribution. Non-normal data is common in
    manufacturing (e.g., tool wear produces right-skewed distributions,
    surface roughness follows lognormal, fatigue life follows Weibull).
    Standard Cp/Cpk formulas assume normality and produce misleading results
    on non-normal data.

STANDARDS:
    - Box, G.E.P. & Cox, D.R. (1964), "An Analysis of Transformations",
      JRSS Series B, 26(2), pp.211-252 -- Box-Cox power transformation
    - ISO 21747:2006, "Statistical methods -- Process performance and
      capability statistics for measured quality characteristics",
      Section 7: Percentile method for non-normal capability
    - ASTM E2587-16, Section 10: Non-normal data and transformations
    - Montgomery (2019), "Introduction to Statistical Quality Control",
      8th Ed., Chapter 7, Section 7.5: Process Capability Analysis with
      Non-Normal Data
    - Burnham, K.P. & Anderson, D.R. (2002), "Model Selection and
      Multimodel Inference", Springer -- AICc as selection criterion
    - D'Agostino, R.B. & Stephens, M.A. (1986), "Goodness-of-Fit
      Techniques", Marcel Dekker -- Anderson-Darling and KS tests
    - Lilliefors, H.W. (1967), "On the Kolmogorov-Smirnov test for
      normality with mean and variance unknown", JASA 62(318), pp.399-402
    - Johnson, N.L. (1949), "Systems of frequency curves generated by
      methods of translation", Biometrika, 36, pp.149-176 -- Johnson
      distribution families

ARCHITECTURE:
    Three non-normal capability methods, applied in cascade:
      1. Box-Cox transformation: Transform data to approximate normality,
         then apply standard Cp/Cpk formulas in the transformed space.
         Spec limits are also transformed. Within-subgroup sigma is
         transformed via the delta method.
      2. Percentile method (ISO 21747): Use P0.135, P50, P99.865 percentiles
         (the normal-equivalent 3-sigma points) to estimate process spread
         without distributional assumptions.
      3. Distribution fitting: Fit multiple parametric families (normal,
         lognormal, Weibull, gamma, Johnson SU/SB) via MLE, rank by AICc,
         and compute capability from the best-fitting distribution's
         percentiles.

    The "auto" method cascades: try Box-Cox first; if it fails (e.g.,
    non-positive data), fall back to distribution fitting; always compute
    percentile method as a robust fallback.

KEY DECISIONS:
    - Model selection uses AICc (corrected Akaike Information Criterion),
      not goodness-of-fit p-values, as the PRIMARY ranking criterion.
      AICc balances fit quality against model complexity and is valid
      regardless of parameter estimation method.
      Ref: Burnham & Anderson (2002), Chapter 2.
    - GoF tests (Anderson-Darling for normal, KS for others) serve as
      ADVISORY absolute rejection filters only. The KS test with estimated
      parameters (Lilliefors effect) has inflated Type I error, making its
      p-values optimistically biased. See _anderson_darling_test docstring.
    - Box-Cox within-sigma transformation uses the delta method:
      sigma_t = sigma * |x-bar^(lambda-1)|. This is adequate when
      CV < ~30%. For the original-scale mean, we use np.mean(original_values)
      to avoid Jensen's inequality bias from back-transformation.
    - Q-Q plots use Blom plotting positions p_i = (i - 3/8) / (n + 1/4),
      which are recommended for probability plots across distribution
      families.
"""

