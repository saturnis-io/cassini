# Variable metrics require study_type in (crossed_anova, range_method, nested_anova)
MSA_VARIABLE_METRICS = {
    "pct_study_grr", "pct_study_ev", "pct_study_av", "ndc",
    "pct_tolerance_grr", "f_operator", "f_part", "f_interaction",
}
# F-statistics are only available for crossed ANOVA
MSA_F_STAT_METRICS = {"f_operator", "f_part", "f_interaction"}
# Attribute metrics require study_type == "attribute_agreement"
MSA_ATTRIBUTE_METRICS = {"fleiss_kappa", "cohens_kappa", "between_appraiser"}


@router.get(
    "/msa/{metric_type}/{study_id}",
    response_model=ExplanationResponse,
)
async def explain_msa_metric(
    metric_type: str,
    study_id: int,
    db: AsyncSession = Depends(get_db_session),
    current_user: User = Depends(get_current_user),
):
    """Return step-by-step explanation of an MSA metric.

    Supports variable Gage R&R metrics (%Study, ndc, F-statistics) and
    attribute Kappa metrics (Cohen's, Fleiss').
    """
    if metric_type not in MSA_METRIC_DEFINITIONS:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Unknown MSA metric: {metric_type}",
        )

    # Load study with operators and parts
    stmt = (
        select(MSAStudy)
        .where(MSAStudy.id == study_id)
        .options(
            selectinload(MSAStudy.operators),
            selectinload(MSAStudy.parts),
        )
    )
    result = await db.execute(stmt)
    study = result.scalar_one_or_none()
    if study is None:
        raise HTTPException(status_code=404, detail="MSA study not found")

    # RBAC check
    check_plant_role(current_user, study.plant_id, "operator")

    # Determine study category
    is_variable = study.study_type in ("crossed_anova", "range_method", "nested_anova")
    is_attribute = study.study_type == "attribute_agreement"

    # Validate metric vs study type
    if metric_type in MSA_VARIABLE_METRICS and not is_variable:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Metric '{metric_type}' requires a variable MSA study (crossed_anova/range_method/nested_anova)",
        )
    if metric_type in MSA_ATTRIBUTE_METRICS and not is_attribute:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Metric '{metric_type}' requires an attribute MSA study",
        )
    if metric_type in MSA_F_STAT_METRICS and study.study_type != "crossed_anova":
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"F-statistic metrics are only available for crossed ANOVA studies",
        )

    # Load measurements
    meas_result = await db.execute(
        select(MSAMeasurement).where(MSAMeasurement.study_id == study_id)
    )
    measurements = list(meas_result.scalars().all())

    if not measurements:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="No measurements found for this study",
        )

    # Build operator/part index maps by sequence_order
    sorted_operators = sorted(study.operators, key=lambda o: o.sequence_order)
    sorted_parts = sorted(study.parts, key=lambda p: p.sequence_order)
    op_index = {op.id: i for i, op in enumerate(sorted_operators)}
    part_index = {p.id: i for i, p in enumerate(sorted_parts)}

    n_ops = len(sorted_operators)
    n_parts = len(sorted_parts)
    n_reps = study.num_replicates

    collector = ExplanationCollector()

    if is_variable:
        # Reshape to 3D: [operators][parts][replicates] -> float
        data_3d: list[list[list[float | None]]] = [
            [[None] * n_reps for _ in range(n_parts)]
            for _ in range(n_ops)
        ]
        for m in measurements:
            oi = op_index.get(m.operator_id)
            pi = part_index.get(m.part_id)
            if oi is None or pi is None:
                continue
            ri = m.replicate_num - 1
            if 0 <= ri < n_reps:
                data_3d[oi][pi][ri] = m.value

        # Validate completeness
        for i in range(n_ops):
            for j in range(n_parts):
                for k in range(n_reps):
                    if data_3d[i][j][k] is None:
                        raise HTTPException(
                            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                            detail="Measurement matrix is incomplete",
                        )

        # Compute tolerance from linked characteristic if study has one
        tolerance = study.tolerance
        if tolerance is None and study.characteristic_id is not None:
            from cassini.db.models.characteristic import Characteristic

            char_stmt = select(Characteristic).where(
                Characteristic.id == study.characteristic_id
            )
            char_result = await db.execute(char_stmt)
            char = char_result.scalar_one_or_none()
            if char is not None and char.usl is not None and char.lsl is not None:
                tolerance = char.usl - char.lsl

        # Run appropriate engine method with collector
        engine = GageRREngine()
        try:
            if study.study_type == "crossed_anova":
                grr_result = engine.calculate_crossed_anova(
                    data_3d, tolerance, collector=collector,  # type: ignore[arg-type]
                )
            elif study.study_type == "range_method":
                grr_result = engine.calculate_range_method(
                    data_3d, tolerance, collector=collector,  # type: ignore[arg-type]
                )
            else:  # nested_anova
                grr_result = engine.calculate_nested_anova(
                    data_3d, tolerance, collector=collector,  # type: ignore[arg-type]
                )
        except ValueError:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail="Insufficient data for MSA calculation",
            )

        # Extract metric value from result
        metric_value_map = {
            "pct_study_grr": grr_result.pct_study_grr,
            "pct_study_ev": grr_result.pct_study_ev,
            "pct_study_av": grr_result.pct_study_av,
            "ndc": float(grr_result.ndc),
            "pct_tolerance_grr": grr_result.pct_tolerance_grr,
            "f_operator": grr_result.anova_table["operator"]["F"] if grr_result.anova_table else None,
            "f_part": grr_result.anova_table["part"]["F"] if grr_result.anova_table else None,
            "f_interaction": grr_result.anova_table["interaction"]["F"] if grr_result.anova_table else None,
        }
        metric_value = metric_value_map.get(metric_type)
        if metric_value is None:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail=f"Cannot compute {metric_type} for this study",
            )

        citation = MSA_CITATION

    else:
        # Attribute study
        attr_3d: list[list[list[str | None]]] = [
            [[None] * n_reps for _ in range(n_parts)]
            for _ in range(n_ops)
        ]
        for m in measurements:
            oi = op_index.get(m.operator_id)
            pi = part_index.get(m.part_id)
            if oi is None or pi is None:
                continue
            ri = m.replicate_num - 1
            if 0 <= ri < n_reps:
                attr_3d[oi][pi][ri] = m.attribute_value

        # Validate completeness
        for i in range(n_ops):
            for j in range(n_parts):
                for k in range(n_reps):
                    if attr_3d[i][j][k] is None:
                        raise HTTPException(
                            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                            detail="Attribute measurement matrix is incomplete",
                        )

        operator_names = [op.name for op in sorted_operators]

        attr_engine = AttributeMSAEngine()
        try:
            attr_result = attr_engine.calculate(
                attr_3d,  # type: ignore[arg-type]
                operator_names=operator_names,
                collector=collector,
            )
        except ValueError:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail="Insufficient data for attribute MSA calculation",
            )

        # Extract metric value
        if metric_type == "fleiss_kappa":
            metric_value = attr_result.fleiss_kappa
        elif metric_type == "between_appraiser":
            metric_value = attr_result.between_appraiser
        elif metric_type == "cohens_kappa":
            # Return the first pairwise kappa (or average if preferred)
            if attr_result.cohens_kappa_pairs:
                # Use average of all pairwise kappas
                kappa_values = list(attr_result.cohens_kappa_pairs.values())
                metric_value = sum(kappa_values) / len(kappa_values)
            else:
                raise HTTPException(
                    status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                    detail="No pairwise Cohen's Kappa values available",
                )
        else:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail=f"Cannot compute {metric_type} for this study",
            )

        citation = ATTRIBUTE_MSA_CITATION

    display_name, formula_latex = MSA_METRIC_DEFINITIONS[metric_type]

    # Filter steps by prefix
    prefixes = MSA_METRIC_STEP_PREFIXES.get(metric_type, [])
    relevant_steps = [
        s
        for s in collector.steps
        if any(s.label.startswith(p) for p in prefixes)
    ]

    return ExplanationResponse(
        metric=metric_type,
        display_name=display_name,
        value=metric_value,
        formula_latex=formula_latex,
        steps=[
            ExplanationStepSchema(
                label=s.label,
                formula_latex=s.formula_latex,
                substitution_latex=s.substitution_latex,
                result=s.result,
                note=s.note,
            )
            for s in relevant_steps
        ],
        inputs=collector.inputs,
        citation=CitationSchema(
            standard=citation.standard,
            reference=citation.reference,
            section=citation.section,
        ),
        method=study.study_type,
        warnings=collector.warnings,
    )
