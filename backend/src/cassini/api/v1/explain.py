@router.get(
    "/control-limits/{metric_type}/{characteristic_id}",
    response_model=ExplanationResponse,
)
async def explain_control_limits_metric(
    metric_type: str,
    characteristic_id: int,
    db: AsyncSession = Depends(get_db_session),
    current_user: User = Depends(get_current_user),
):
    """Return step-by-step explanation of a control limit metric.

    Shows how UCL, LCL, center line, or sigma were computed, including
    the method selection (I-MR, R-bar/d2, or S-bar/c4) and all
    intermediate values.
    """
    if metric_type not in CONTROL_LIMITS_METRICS:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Unknown control limits metric: {metric_type}",
        )

    # Plant RBAC check
    plant_id = await resolve_plant_id_for_characteristic(characteristic_id, db)
    check_plant_role(current_user, plant_id, "operator")

    # Load characteristic
    from sqlalchemy import select as sa_select

    from cassini.db.models.characteristic import Characteristic

    stmt = sa_select(Characteristic).where(Characteristic.id == characteristic_id)
    result = await db.execute(stmt)
    char = result.scalar_one_or_none()
    if char is None:
        raise HTTPException(status_code=404, detail="Characteristic not found")

