@router.get(
    "/control-limits/{metric_type}/{characteristic_id}",
    response_model=ExplanationResponse,
)
async def explain_control_limits_metric(
    metric_type: str,
    characteristic_id: int,
    db: AsyncSession = Depends(get_db_session),
    current_user: User = Depends(get_current_user),
):
    """Return step-by-step explanation of a control limit metric.

    Uses stored values (sigma, center_line, UCL, LCL) directly rather than
    replaying calculations. This guarantees the explanation matches the
    displayed values exactly, regardless of when limits were set or how
    many samples have been added since.
    """
    if metric_type not in CONTROL_LIMITS_METRICS:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Unknown control limits metric: {metric_type}",
        )

    # Plant RBAC check
    plant_id = await resolve_plant_id_for_characteristic(characteristic_id, db)
    check_plant_role(current_user, plant_id, "operator")

    # Load characteristic
    from sqlalchemy import select as sa_select

    from cassini.db.models.characteristic import Characteristic

    stmt = sa_select(Characteristic).where(Characteristic.id == characteristic_id)
    result = await db.execute(stmt)
    char = result.scalar_one_or_none()
    if char is None:
        raise HTTPException(status_code=404, detail="Characteristic not found")

    # Only variable charts support control limit explanation
    if char.data_type == "attribute":
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Control limit explanation is not supported for attribute charts",
        )

    # Read stored values — these are the source of truth
    stored_sigma = char.stored_sigma
    stored_center = char.stored_center_line
    subgroup_size = char.subgroup_size
    short_run_mode = getattr(char, "short_run_mode", None)
    target_value = char.target_value

    if stored_sigma is None or stored_center is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="No stored control limits to explain — recalculate limits first",
        )

    # Determine method from subgroup size
    if subgroup_size == 1:
        method = "moving_range"
    elif subgroup_size <= 10:
        method = "r_bar_d2"
    else:
        method = "s_bar_c4"

    # Build explanation from stored values (no sample queries needed)
    collector = ExplanationCollector()

    # Record inputs
    collector.input("n (subgroup size)", subgroup_size)
    collector.input("σ (stored process sigma)", round(stored_sigma, 6))
    collector.input("Center line (stored)", round(stored_center, 6))

    # Step 1: Show sigma source based on method
    if method == "moving_range":
        d2 = get_d2(2)
        mr_bar = stored_sigma * d2  # reverse: sigma = MR-bar / d2
        collector.step(
            label="σ (Process Sigma) — from stored MR-bar/d2",
            formula_latex=r"\sigma = \frac{\overline{MR}}{d_2}",
            substitution_latex=(
                r"\sigma = \frac{"
                + str(round(mr_bar, 6))
                + r"}{"
                + str(d2)
                + r"}"
            ),
            result=stored_sigma,
            note="Stored from last limit calculation (I-MR method, d₂=1.128 for span=2)",
        )
    elif method == "r_bar_d2":
        d2 = get_d2(subgroup_size)
        r_bar = stored_sigma * d2  # reverse: sigma = R-bar / d2
        collector.step(
            label="σ (Process Sigma) — from stored R-bar/d2",
            formula_latex=r"\sigma = \frac{\bar{R}}{d_2}",
            substitution_latex=(
                r"\sigma = \frac{"
                + str(round(r_bar, 6))
                + r"}{"
                + str(d2)
                + r"}"
            ),
            result=stored_sigma,
            note=f"Stored from last limit calculation (R-bar/d₂ method, n={subgroup_size})",
        )
    else:  # s_bar_c4
        c4 = get_c4(subgroup_size)
        s_bar = stored_sigma * c4  # reverse: sigma = S-bar / c4
        collector.step(
            label="σ (Process Sigma) — from stored S-bar/c4",
            formula_latex=r"\sigma = \frac{\bar{S}}{c_4}",
            substitution_latex=(
                r"\sigma = \frac{"
                + str(round(s_bar, 6))
                + r"}{"
                + str(c4)
                + r"}"
            ),
            result=stored_sigma,
            note=f"Stored from last limit calculation (S-bar/c₄ method, n={subgroup_size})",
        )

    # Step 2: Sigma of the mean (for subgroups > 1)
    if subgroup_size > 1:
        sigma_xbar = stored_sigma / math.sqrt(subgroup_size)
        collector.step(
            label="σ_x̄ (Sigma of the Mean)",
            formula_latex=r"\sigma_{\bar{x}} = \frac{\sigma}{\sqrt{n}}",
            substitution_latex=(
                r"\sigma_{\bar{x}} = \frac{"
                + str(round(stored_sigma, 6))
                + r"}{\sqrt{"
                + str(subgroup_size)
                + r"}}"
            ),
            result=sigma_xbar,
        )
        sigma_for_limits = sigma_xbar
    else:
        sigma_for_limits = stored_sigma

    # Step 3: Center line
    collector.step(
        label="X-bar (Center Line)",
        formula_latex=(
            r"\bar{x}" if subgroup_size == 1
            else r"\bar{\bar{x}}"
        ),
        substitution_latex=(
            r"\bar{x} = " + str(round(stored_center, 6))
            if subgroup_size == 1
            else r"\bar{\bar{x}} = " + str(round(stored_center, 6))
        ),
        result=stored_center,
        note="Stored from last limit calculation",
    )

    # Step 4: UCL and LCL from stored values
    computed_ucl = stored_center + 3 * sigma_for_limits
    computed_lcl = stored_center - 3 * sigma_for_limits

    ucl_formula = (
        r"UCL = \bar{x} + 3\sigma" if subgroup_size == 1
        else r"UCL = \bar{\bar{x}} + 3\sigma_{\bar{x}}"
    )
    lcl_formula = (
        r"LCL = \bar{x} - 3\sigma" if subgroup_size == 1
        else r"LCL = \bar{\bar{x}} - 3\sigma_{\bar{x}}"
    )

    collector.step(
        label="UCL (Upper Control Limit)",
        formula_latex=ucl_formula,
        substitution_latex=(
            r"UCL = "
            + str(round(stored_center, 6))
            + r" + 3 \times "
            + str(round(sigma_for_limits, 6))
        ),
        result=computed_ucl,
    )
    collector.step(
        label="LCL (Lower Control Limit)",
        formula_latex=lcl_formula,
        substitution_latex=(
            r"LCL = "
            + str(round(stored_center, 6))
            + r" - 3 \times "
            + str(round(sigma_for_limits, 6))
        ),
        result=computed_lcl,
    )

    # Apply short-run transforms to get displayed values
    display_ucl = computed_ucl
    display_lcl = computed_lcl
    display_center = stored_center
    display_sigma = stored_sigma

    if short_run_mode == "deviation" and target_value is not None:
        display_ucl = computed_ucl - target_value
        display_lcl = computed_lcl - target_value
        display_center = stored_center - target_value
        collector.step(
            label="UCL (Deviation from Target)",
            formula_latex=r"UCL_{dev} = UCL - T",
            substitution_latex=(
                r"UCL_{dev} = "
                + str(round(computed_ucl, 6))
                + r" - "
                + str(round(target_value, 6))
            ),
            result=display_ucl,
            note=f"Short-run deviation mode: shift by target ({target_value})",
        )
        collector.step(
            label="LCL (Deviation from Target)",
            formula_latex=r"LCL_{dev} = LCL - T",
            substitution_latex=(
                r"LCL_{dev} = "
                + str(round(computed_lcl, 6))
                + r" - "
                + str(round(target_value, 6))
            ),
            result=display_lcl,
            note=f"Short-run deviation mode: shift by target ({target_value})",
        )

    elif short_run_mode == "standardized" and target_value is not None and stored_sigma:
        display_ucl = 3.0
        display_lcl = -3.0
        display_center = 0.0
        collector.step(
            label="UCL (Standardized Z-score)",
            formula_latex=r"UCL_Z = +3",
            substitution_latex=r"UCL_Z = +3",
            result=3.0,
            note="Short-run standardized mode: Z = (x̄ − T) / σ_x̄, limits are fixed ±3",
        )
        collector.step(
            label="LCL (Standardized Z-score)",
            formula_latex=r"LCL_Z = -3",
            substitution_latex=r"LCL_Z = -3",
            result=-3.0,
            note="Short-run standardized mode: Z = (x̄ − T) / σ_x̄, limits are fixed ±3",
        )

    # Map metric to displayed value
    metric_value_map = {
        "ucl": display_ucl,
        "lcl": display_lcl,
        "center_line": display_center,
        "sigma": display_sigma,
    }
    metric_value = metric_value_map[metric_type]

    display_name, formula_latex = CONTROL_LIMITS_DEFINITIONS[metric_type]

    # Filter steps to only those relevant to the requested metric
    prefixes = CONTROL_LIMITS_STEP_PREFIXES.get(metric_type, [])
    relevant_steps = [
        s
        for s in collector.steps
        if any(s.label.startswith(p) for p in prefixes)
    ]

    return ExplanationResponse(
        metric=metric_type,
        display_name=display_name,
        value=metric_value,
        formula_latex=formula_latex,
        steps=[
            ExplanationStepSchema(
                label=s.label,
                formula_latex=s.formula_latex,
                substitution_latex=s.substitution_latex,
                result=s.result,
                note=s.note,
            )
            for s in relevant_steps
        ],
        inputs=collector.inputs,
        citation=CitationSchema(
            standard=CONTROL_LIMITS_CITATION.standard,
            reference=CONTROL_LIMITS_CITATION.reference,
            section=CONTROL_LIMITS_CITATION.section,
        ),
        method=method,
        sigma_estimator=method,
        warnings=collector.warnings,
    )


