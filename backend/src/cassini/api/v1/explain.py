@router.get(
    "/attribute/{metric_type}/{characteristic_id}",
    response_model=ExplanationResponse,
)
async def explain_attribute_metric(
    metric_type: str,
    characteristic_id: int,
    db: AsyncSession = Depends(get_db_session),
    current_user: User = Depends(get_current_user),
):
    """Return step-by-step explanation of an attribute SPC metric.

    Supports center_line, ucl, lcl for all attribute chart types (p, np, c, u),
    and sigma_z (Laney correction) for p and u charts.
    """
    if metric_type not in ATTRIBUTE_METRICS:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Unknown attribute metric: {metric_type}",
        )

    # Plant RBAC check
    plant_id = await resolve_plant_id_for_characteristic(characteristic_id, db)
    check_plant_role(current_user, plant_id, "operator")

    # Load characteristic
    from sqlalchemy import select as sa_select

    from cassini.db.models.characteristic import Characteristic

    stmt = sa_select(Characteristic).where(Characteristic.id == characteristic_id)
    result = await db.execute(stmt)
    char = result.scalar_one_or_none()
    if char is None:
        raise HTTPException(status_code=404, detail="Characteristic not found")

    # Only attribute charts support this endpoint
    if char.data_type != "attribute":
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="This endpoint only supports attribute charts",
        )

    chart_type = char.attribute_chart_type
    if not chart_type:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Characteristic has no attribute_chart_type configured",
        )

    # sigma_z only applies to p and u charts
    if metric_type == "sigma_z" and chart_type not in ("p", "u"):
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Laney sigma_z correction only applies to p and u charts",
        )

    # Load attribute samples
    repo = SampleRepository(db)
    samples = await repo.get_by_characteristic(char_id=characteristic_id)

    # Filter excluded and reshape to dicts
    sample_dicts: list[dict] = []
    for s in samples:
        if s.is_excluded:
            continue
        d: dict = {"defect_count": s.defect_count}
        if s.sample_size is not None:
            d["sample_size"] = s.sample_size
        if s.units_inspected is not None:
            d["units_inspected"] = s.units_inspected
        sample_dicts.append(d)

    if len(sample_dicts) < 2:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="Need at least 2 non-excluded attribute samples",
        )

    # Run attribute limit calculation with collector
    collector = ExplanationCollector()
    try:
        limits = calculate_attribute_limits(chart_type, sample_dicts, collector=collector)
    except ValueError:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="Cannot compute attribute limits for this configuration",
        )

    # For sigma_z, run Laney calculation as well
    sigma_z_value: float | None = None
    if metric_type == "sigma_z":
        if len(sample_dicts) < 3:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail="Need at least 3 samples for Laney sigma_z calculation",
            )
        sigma_z_value = calculate_laney_sigma_z(
            chart_type, sample_dicts, limits.center_line, collector=collector,
        )

    # Map metric to value
    metric_value_map: dict[str, float] = {
        "center_line": limits.center_line,
        "ucl": limits.ucl,
        "lcl": limits.lcl,
    }
    if sigma_z_value is not None:
        metric_value_map["sigma_z"] = sigma_z_value

    metric_value = metric_value_map.get(metric_type)
    if metric_value is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"Cannot compute {metric_type} for this characteristic",
        )

    display_name, formula_latex = ATTRIBUTE_METRIC_DEFINITIONS[metric_type]

    # Filter steps to only those relevant to the requested metric
    prefixes = ATTRIBUTE_STEP_PREFIXES.get(metric_type, [])
    relevant_steps = [
        s
        for s in collector.steps
        if any(s.label.startswith(p) for p in prefixes)
    ]

    return ExplanationResponse(
        metric=metric_type,
        display_name=display_name,
        value=metric_value,
        formula_latex=formula_latex,
        steps=[
            ExplanationStepSchema(
                label=s.label,
                formula_latex=s.formula_latex,
                substitution_latex=s.substitution_latex,
                result=s.result,
                note=s.note,
            )
            for s in relevant_steps
        ],
        inputs=collector.inputs,
        citation=CitationSchema(
            standard=ATTRIBUTE_SPC_CITATION.standard,
            reference=ATTRIBUTE_SPC_CITATION.reference,
            section=ATTRIBUTE_SPC_CITATION.section,
        ),
        method=chart_type,
        warnings=collector.warnings,
    )


