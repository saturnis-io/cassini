@router.get(
    "/capability/{metric_type}/{characteristic_id}",
    response_model=ExplanationResponse,
)
async def explain_capability_metric(
    metric_type: str,
    characteristic_id: int,
    limit: int | None = Query(None, ge=2, le=10000, description="Max samples (matches dashboard limit)"),
    start_date: str | None = Query(None, description="ISO start date (matches dashboard range)"),
    end_date: str | None = Query(None, description="ISO end date (matches dashboard range)"),
    db: AsyncSession = Depends(get_db_session),
    current_user: User = Depends(get_current_user),
):
    """Return step-by-step explanation of a capability metric.

    Pass the same limit/start_date/end_date the dashboard uses so the
    explanation matches the displayed value exactly.
    """
    if metric_type not in CAPABILITY_METRICS:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Unknown capability metric: {metric_type}",
        )

    # Plant RBAC check (resolve plant_id through hierarchy)
    plant_id = await resolve_plant_id_for_characteristic(characteristic_id, db)
    check_plant_role(current_user, plant_id, "operator")

    # Load characteristic
    from sqlalchemy import select as sa_select

    from cassini.db.models.characteristic import Characteristic

    stmt = sa_select(Characteristic).where(Characteristic.id == characteristic_id)
    result = await db.execute(stmt)
    char = result.scalar_one_or_none()
    if char is None:
        raise HTTPException(status_code=404, detail="Characteristic not found")

    # Only variable charts support capability explanation
    if char.data_type == "attribute":
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Capability analysis is not supported for attribute charts",
        )

    if char.usl is None and char.lsl is None:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="At least one specification limit (USL or LSL) must be set",
        )

    # Two modes of operation:
    # 1. With chart options (start_date/end_date/limit) — matches the dashboard's
    #    quickStats, which uses subgroup MEANS.  Sigma from means too.
    # 2. Without chart options — matches the capability GET endpoint, which
    #    flattens individual measurements and uses stored_sigma (R-bar/d2).
    repo = SampleRepository(db)
    has_chart_options = bool(start_date or end_date or limit)

    if start_date or end_date:
        # Date-range mode: match the dashboard's time filter
        sd = datetime.fromisoformat(start_date) if start_date else None
        ed = datetime.fromisoformat(end_date) if end_date else None
        samples = await repo.get_by_characteristic(
            char_id=characteristic_id,
            start_date=sd,
            end_date=ed,
        )
        # Filter excluded samples (get_by_characteristic doesn't do this)
        samples = [s for s in samples if not s.is_excluded]
        # Apply limit (most recent N within the range)
        if limit and len(samples) > limit:
            samples = samples[-limit:]
        # Compute subgroup means — matching dashboard's stdPts.map(p => p.mean)
        values: list[float] = []
        for s in samples:
            meas = [m.value for m in s.measurements]
            if meas:
                values.append(sum(meas) / len(meas))
    else:
        # No chart options — match the capability GET endpoint exactly.
        # CapabilityCard uses this path, which flattens individual measurements.
        window = limit if limit else 1000
        sample_data = await repo.get_rolling_window_data(
            char_id=characteristic_id,
            window_size=window,
            exclude_excluded=True,
        )
        values = []
        for sd_item in sample_data:
            values.extend(sd_item["values"])

    if len(values) < 2:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="Need at least 2 measurements",
        )

    # Sigma strategy: when called with chart options (matching a dashboard view),
    # compute sigma from the subgroup means — this is exactly what the dashboard
    # does in its quickStats useMemo.  Without chart options (e.g. from
    # CapabilityCard), use stored_sigma (R-bar/d2) for AIAG-correct Cpk.
    sigma_fallback = False
    if has_chart_options:
        # Match dashboard: sigma = sample std dev of subgroup means
        sigma_within = float(np.std(np.asarray(values, dtype=np.float64), ddof=1))
        sigma_fallback = True
    else:
        sigma_within = char.stored_sigma
        if sigma_within is None or sigma_within <= 0:
            sigma_within = float(np.std(np.asarray(values, dtype=np.float64), ddof=1))
            sigma_fallback = True

    # Run capability with collector
    collector = ExplanationCollector()
    if has_chart_options:
        collector.warn(
            "Using sample \u03c3 of subgroup means to match the dashboard display. "
            "Cp/Cpk will equal Pp/Ppk."
        )
    elif sigma_fallback:
        collector.warn(
            "Within-subgroup sigma (\u03c3_w) not stored \u2014 using sample standard "
            "deviation as estimate. Cp/Cpk values equal Pp/Ppk."
        )
    cap_result = calculate_capability(
        values=values,
        usl=char.usl,
        lsl=char.lsl,
        target=char.target_value,
        sigma_within=sigma_within,
        collector=collector,
    )

    # Extract the requested metric value
    metric_value = getattr(cap_result, metric_type, None)
    if metric_value is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"Cannot compute {metric_type} -- check specification limits and data",
        )

    display_name, formula_latex = METRIC_DEFINITIONS[metric_type]

    # Filter steps to only those relevant to the requested metric
    prefixes = METRIC_STEP_PREFIXES.get(metric_type, [])
    relevant_steps = [
        s
        for s in collector.steps
        if any(s.label.startswith(p) for p in prefixes)
    ]

    return ExplanationResponse(
        metric=metric_type,
        display_name=display_name,
        value=metric_value,
        formula_latex=formula_latex,
        steps=[
            ExplanationStepSchema(
                label=s.label,
                formula_latex=s.formula_latex,
                substitution_latex=s.substitution_latex,
                result=s.result,
                note=s.note,
            )
            for s in relevant_steps
        ],
        inputs=collector.inputs,
        citation=CitationSchema(
            standard=CAPABILITY_CITATION.standard,
            reference=CAPABILITY_CITATION.reference,
            section=CAPABILITY_CITATION.section,
        ),
        method="normal",
        warnings=collector.warnings,
    )


